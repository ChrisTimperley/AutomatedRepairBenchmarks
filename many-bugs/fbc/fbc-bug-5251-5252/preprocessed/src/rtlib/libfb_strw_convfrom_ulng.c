{
    const wchar_t *p, *r;
    int radix;


 p = fb_wstr_SkipChar( src, len, 32 );

 len -= fb_wstr_CalcDiff( src, p );
 if( len < 1 )
  return 0;

 radix = 10;
 r = p;
 if( (len >= 2) && (*r++ == L'&') )
 {
  switch( *r++ )
  {
   case L'h':
   case L'H':
    radix = 16;
    break;
   case L'o':
   case L'O':
    radix = 8;
    break;
   case L'b':
   case L'B':
    radix = 2;
    break;
  }

  if( radix != 10 )
  {



   p = r;

  }
 }

 return wcstoull( p, ((void *)0), radix );
}


